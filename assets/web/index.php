$book['01']['judge'] = 
'		THE CREATIVE works sublime success,\n
		Furthering through perseverance.\n 
		According to the original meaning, the attributes [sublimity, potentiality of success, power to further, perseverance] are paired. When an individual draws this oracle, it means that success will come to him from the primal depths of the universe and that everything depends upon his seeking his happiness and that of others in one way only, that is, by perseverance in what is right.\n
		The specific meanings of the four attributes became the subject of speculation at an early date. The Chinese word here rendered by "sublime" means literally "head," "origin," "great. " This is why Confucius says in explaining it: "Great indeed is the generating power of the Creative; all beings owe their beginning to it. This power permeates all heaven. " For this attribute inheres in the other three as well.\n
		The beginning of all things lies still in the beyond in the form of ideas that have yet to become real. But the Creative furthermore has power to lend form to these archetypes of ideas. This is indicated in the word success, and the process is represented by an image from nature: "The clouds pass and the rain does its work, and all individual beings flow into their forms. "\n
		Applied to the human world, these attributes show the great man the way to notable success: "Because he sees with great clarity and cause and effects, he completes the six steps at the right time and mounts toward heaven on them at the right time, as though on sic dragons. " The six steps are the six different positions given in the hexagram, which are represented later by the dragon symbol. Here it is shown that the way to success lies in apprehending and giving actuality to the way of the universe [Tao], which, as a law running through end and beginning, brings about all phenomena in time. Thus each step attained forthwith becomes a preparation for the next. Time is no longer a hindrance but the means of making actual what is potential.\n
		The act of creation having found expression in the two attributes sublimity and success, the work of conservation is shown to be a continuous actualization and differentiation of form. This is expressed in the two terms "furthering" (literally, "creating that which accords with the nature of a given being") and "persevering" (literally, "correct and firm"). "The course of the Creative alters and shapes beings until each attains its true, specific nature, then it keeps them in conformity with the Great Harmony. Thus does it show itself to further through perseverance. "\n
		In relation to the human sphere, this shows how the great man brings peace and security to the world through his activity in creating order: "He towers high above the multitude of beings, and all lands are united in peace. "\n
		Another line of speculation goes still further in separating the words "sublime," "success," "furthering," "perseverance," and parallels them with the four cardinal virtues in humanity. To sublimity, which, as the fundamental principle, embraces all the other attributes, it links love. To the attribute success are linked the morals, which regulate and organize expressions of love and thereby make them successful. The attribute furthering is correlated with justice, which creates the conditions in which each receives that which accords with his being, that which is due him and which constitutes his happiness. The attribute perseverance is correlated with wisdom, which discerns the immutable laws of all that happens and can therefore bring about enduring conditions. These speculations, already broached in the commentary called WÃªn Yen , later formed the bridge connecting the philosophy of the "five stages (elements) of change," as laid down in the Book of History (Shu Ching) with the philosophy of the Book of Changes, which is based solely on the polarity of positive and negative principles. In the course of time this combination of the two systems of thought opened the way for an increasingly intricate number symbolism.';
$book['01']['image'] = 
'   The movement of heaven is full of power.\n
		Thus the superior man makes himself strong and untiring.\n 
		Since there is only one heaven, the doubling of the trigram Ch\'ien, of which heaven is the image, indicates the movement of heaven. One complete revolution of heaven makes a day, and the repetition of the trigram means that each day is followed by another. This creates the idea of time. Since it is the same heaven moving with untiring power, there is also created the idea of duration both in and beyond time, a movement that never stops nor slackens, just as one day follows another in an unending course. This duration in time is the image of the power inherent in the Creative.\n
		With this image as a model, the sage learns how best to develop himself so that his influence may endure. He must make himself strong in every way, by consciously casting out all that is inferior and degrading. Thus he attains that tirelessness which depends upon consciously limiting the fields of his activity.';

